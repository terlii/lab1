def cyclic_shift_left(array):
    """
    –û—Å—É—â–µ—Å—Ç–≤–ª—è–µ—Ç —Ü–∏–∫–ª–∏—á–µ—Å–∫–∏–π —Å–¥–≤–∏–≥ –º–∞—Å—Å–∏–≤–∞ –≤–ª–µ–≤–æ –Ω–∞ –æ–¥–Ω—É –ø–æ–∑–∏—Ü–∏—é.
    
    Args:
        array (list): –ò—Å—Ö–æ–¥–Ω—ã–π –º–∞—Å—Å–∏–≤
        
    Returns:
        list: –ù–æ–≤—ã–π –º–∞—Å—Å–∏–≤ —Å–æ —Å–¥–≤–∏–≥–æ–º
    """
    if not array or len(array) <= 1:
        return array.copy()
    
    # –°–æ–∑–¥–∞–µ–º –∫–æ–ø–∏—é –∏ –≤—ã–ø–æ–ª–Ω—è–µ–º —Å–¥–≤–∏–≥
    shifted = array[1:] + [array[0]]
    return shifted

def cyclic_shift_left_inplace(array):
    """
    –û—Å—É—â–µ—Å—Ç–≤–ª—è–µ—Ç —Ü–∏–∫–ª–∏—á–µ—Å–∫–∏–π —Å–¥–≤–∏–≥ –º–∞—Å—Å–∏–≤–∞ –≤–ª–µ–≤–æ –Ω–∞ –æ–¥–Ω—É –ø–æ–∑–∏—Ü–∏—é (–∏–∑–º–µ–Ω—è–µ—Ç –∏—Å—Ö–æ–¥–Ω—ã–π).
    
    Args:
        array (list): –ò—Å—Ö–æ–¥–Ω—ã–π –º–∞—Å—Å–∏–≤
        
    Returns:
        list: –¢–æ—Ç –∂–µ –º–∞—Å—Å–∏–≤ –ø–æ—Å–ª–µ —Å–¥–≤–∏–≥–∞
    """
    if not array or len(array) <= 1:
        return array
    
    first = array[0]
    for i in range(len(array) - 1):
        array[i] = array[i + 1]
    array[-1] = first
    
    return array

def cyclic_shift_left_n(array, n):
    """
    –û—Å—É—â–µ—Å—Ç–≤–ª—è–µ—Ç —Ü–∏–∫–ª–∏—á–µ—Å–∫–∏–π —Å–¥–≤–∏–≥ –º–∞—Å—Å–∏–≤–∞ –≤–ª–µ–≤–æ –Ω–∞ n –ø–æ–∑–∏—Ü–∏–π.
    
    Args:
        array (list): –ò—Å—Ö–æ–¥–Ω—ã–π –º–∞—Å—Å–∏–≤
        n (int): –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–∑–∏—Ü–∏–π —Å–¥–≤–∏–≥–∞
        
    Returns:
        list: –ù–æ–≤—ã–π –º–∞—Å—Å–∏–≤ —Å–æ —Å–¥–≤–∏–≥–æ–º
    """
    if not array or len(array) <= 1:
        return array.copy()
    
    n = n % len(array)  # –ù–æ—Ä–º–∞–ª–∏–∑—É–µ–º —Å–¥–≤–∏–≥
    if n == 0:
        return array.copy()
    
    # –í—ã–ø–æ–ª–Ω—è–µ–º —Å–¥–≤–∏–≥
    shifted = array[n:] + array[:n]
    return shifted

